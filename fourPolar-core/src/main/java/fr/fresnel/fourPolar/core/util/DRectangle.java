package fr.fresnel.fourPolar.core.util;

/**
 * A discrete rectangle
 */
public class DRectangle {
    public int xtop;
    public int ytop;
    public int width;
    public int height;

    public DRectangle(int xtop, int ytop, int width, int height){
        this.xtop = xtop;
        this.ytop = ytop;
        this.width = width;
        this.height = height;
    }

}